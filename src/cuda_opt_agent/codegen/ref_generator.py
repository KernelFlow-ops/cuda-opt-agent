def wrap_with_extern_c(kernel_code: str, operator: str, *,
                        extern_c_params: str = "", version: str = "v0") -> str:
    """确保 CUDA 代码包含 extern "C" 入口。"""
    if 'extern "C"' in kernel_code:
        return kernel_code
    template = '''// {operator} kernel — {version}
// extern "C" wrapper for unified benchmarking

{code}

extern "C" void {operator}_kernel({params}) {{
    // TODO: Configure grid/block and launch kernel
    // Example:
    // dim3 block(256);
    // dim3 grid((N + 255) / 256);
    // kernel_impl<<<grid, block>>>(...);
    // cudaDeviceSynchronize();
}}
'''
    return template.format(operator=operator, version=version,
                            code=kernel_code, params=extern_c_params)


def build_extern_c_params(operator: str, dtypes: dict | None = None) -> str:
    """构建 extern C 参数签名。"""
    dtype = "fp16"
    if dtypes:
        dtype = list(dtypes.values())[0]
    ct = _c_dtype(dtype)
    op = operator.lower()
    if op == "gemm":
        return f"const {ct}* A, const {ct}* B, {ct}* C, int M, int N, int K"
    elif op == "softmax":
        return f"const {ct}* input, {ct}* output, int B, int N"
    elif op == "layernorm":
        return f"const {ct}* input, const {ct}* gamma, const {ct}* beta, {ct}* output, int B, int N, float epsilon"
    else:
        return f"const {ct}* input, {ct}* output, int N"
