def generate_ref_py(
    operator: str, *,
    shapes: dict[str, list[int]] | None = None,
    shape_profiles: list[dict] | None = None,
    dtypes: dict[str, str] | None = None,
    default_dtype: str = "fp16",
    output_dir: str | Path = ".",
) -> Path:
    """生成 ref.py。"""
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    td = _torch_dtype(default_dtype)
    cd = _c_dtype(default_dtype)
    content = _HEADER.format(operator=operator, torch_dtype=td)

    op = operator.lower()
    if op == "gemm":
        M, N, K = 4096, 4096, 4096
        if shapes:
            A = shapes.get("A", [4096, 4096]); B = shapes.get("B", [4096, 4096])
            M, K, N = A[0], A[1], B[1]
        elif shape_profiles:
            p = shape_profiles[0]; M, N, K = p.get("M", 4096), p.get("N", 4096), p.get("K", 4096)
        content += _GEMM_BODY.format(M=M, N=N, K=K, operator=operator, c_dtype=cd)
    elif op == "softmax":
        B, N = 4096, 4096
        if shape_profiles:
            p = shape_profiles[0]; B, N = p.get("B", 4096), p.get("N", 4096)
        content += _SOFTMAX_BODY.format(B=B, N=N, operator=operator, c_dtype=cd)
    elif op == "layernorm":
        B, N = 4096, 4096
        if shape_profiles:
            p = shape_profiles[0]; B, N = p.get("B", 4096), p.get("N", 4096)
        content += _LAYERNORM_BODY.format(B=B, N=N, operator=operator, c_dtype=cd)
    else:
        ds = [4096]
        if shapes:
            ds = list(shapes.values())[0] if shapes else [4096]
        elif shape_profiles:
            ds = list(shape_profiles[0].values())
        content += _GENERIC_BODY.format(default_shape=ds, operator=operator, c_dtype=cd)

    content += _FOOTER
    path = output_dir / "ref.py"
    path.write_text(content, encoding="utf-8")
    logger.info("Generated ref.py at %s", path)
    return path


