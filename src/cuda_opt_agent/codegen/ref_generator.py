def _resolve_torch_dtype(name: str | None) -> torch.dtype:
    if name is None:
        return DEFAULT_DTYPE
    normalized = name.lower().replace("torch.", "")
    aliases = {
        "fp16": torch.float16,
        "float16": torch.float16,
        "half": torch.float16,
        "bf16": torch.bfloat16,
        "bfloat16": torch.bfloat16,
        "fp32": torch.float32,
        "float32": torch.float32,
        "float": torch.float32,
        "fp64": torch.float64,
        "float64": torch.float64,
        "double": torch.float64,
    }
    if normalized in aliases:
        return aliases[normalized]
    if hasattr(torch, normalized):
        value = getattr(torch, normalized)
        if isinstance(value, torch.dtype):
            return value
    raise ValueError(f"Unsupported dtype: {name}")


def _parse_scalar_or_list(value: str) -> Any:
    value = value.strip().strip("[]")
    if "," in value:
        return [int(part.strip()) for part in value.split(",") if part.strip()]
    try:
        return int(value)
    except ValueError:
        return float(value)


def _parse_shape_kwargs(items: list[str], unknown: list[str] | None = None) -> dict[str, Any]:
    result: dict[str, Any] = {}
    tokens = list(items or [])
    tokens.extend(token for token in (unknown or []) if "=" in token and not token.startswith("--"))
    for token in tokens:
        if "=" not in token:
            continue
        key, value = token.split("=", 1)
        result[key.strip()] = _parse_scalar_or_list(value)
    return result


def _generate_inputs_from_cli(shape_kwargs: dict[str, Any], *, dtype: torch.dtype,
                              device: str, epsilon: float) -> dict[str, Any]:
    params = inspect.signature(generate_inputs).parameters
    kw = {k: v for k, v in shape_kwargs.items() if k in params}
    if "epsilon" in params:
        kw["epsilon"] = epsilon
    if "dtype" in params:
        kw["dtype"] = dtype
    if "device" in params:
        kw["device"] = device
    return generate_inputs(**kw)


def _move_inputs_to_device(inputs: dict[str, Any], device: str) -> dict[str, Any]:
    if not str(device).startswith("cuda"):
        return inputs
    moved: dict[str, Any] = {}
    for key, value in inputs.items():
        moved[key] = value.to(device, non_blocking=True) if isinstance(value, torch.Tensor) else value
    torch.cuda.synchronize()
    return moved


def _generate_cuda_inputs(shape_kwargs: dict[str, Any], *, dtype: torch.dtype,
                          device: str, epsilon: float) -> dict[str, Any]:
    # Initialize on CPU so ncu does not profile PyTorch random-fill kernels before
    # the target CUDA kernel launches.
    init_device = "cpu" if str(device).startswith("cuda") else device
    inputs = _generate_inputs_from_cli(shape_kwargs, dtype=dtype, device=init_device, epsilon=epsilon)
    return _move_inputs_to_device(inputs, device)


def validate_correctness(cuda_output: torch.Tensor, ref_output: torch.Tensor, *,
                         atol: float = 1e-2, rtol: float = 1e-2,
                         sample_ratio: float = 1.0) -> dict[str, Any]:
    if sample_ratio < 1.0:
        numel = cuda_output.numel()
        n = max(1, int(numel * sample_ratio))
        idx = torch.randperm(numel, device=cuda_output.device)[:n]
        cf, rf = cuda_output.flatten()[idx], ref_output.flatten()[idx]
    else:
        cf, rf = cuda_output.flatten(), ref_output.flatten()
    cf = cf.float()
    rf = rf.float()
    max_abs = (cf - rf).abs().max().item()
    max_rel = ((cf - rf).abs() / (rf.abs() + 1e-8)).max().item()
    passed = torch.allclose(cf, rf, atol=atol, rtol=rtol)
    return {"passed": bool(passed), "max_abs_error": max_abs, "max_rel_error": max_rel,
            "atol": atol, "rtol": rtol, "num_elements_checked": cf.numel()}


def _auto_link_flags(cu_file: Path) -> list[str]:
    try:
        text = cu_file.read_text(encoding="utf-8", errors="replace")
    except Exception:
        return []
    flags: list[str] = []
    if "cublasLt.h" in text:
        flags.extend(["-lcublasLt", "-lcublas"])
    elif "cublas_v2.h" in text:
        flags.append("-lcublas")
    if "cudnn.h" in text:
        flags.append("-lcudnn")
    return list(dict.fromkeys(flags))


def compile_cuda_shared(cu_file: str | Path, *, arch: str = "sm_80",
                        output: str | Path | None = None,
                        extra_flags: list[str] | None = None,
                        timeout: int = 120) -> Path:
    cu_path = Path(cu_file).expanduser().resolve()
    if not cu_path.exists():
        raise RuntimeError(f"CUDA source not found: {cu_path}")
    if cu_path.suffix == ".so":
        return cu_path
    nvcc = shutil.which("nvcc")
    if not nvcc:
        raise RuntimeError("nvcc not found; ensure CUDA Toolkit is installed")
    so_path = Path(output).expanduser().resolve() if output else cu_path.with_suffix(".so")
    if so_path.exists() and so_path.stat().st_mtime >= cu_path.stat().st_mtime:
        return so_path
    arch_num = arch.replace("sm_", "")
    cmd = [
        nvcc,
        "-shared",
        "-Xcompiler", "-fPIC",
        "-O3",
        "-lineinfo",
        "-allow-unsupported-compiler",
        "-D_ALLOW_COMPILER_AND_STL_VERSION_MISMATCH",
        f"-gencode=arch=compute_{arch_num},code=sm_{arch_num}",
        f"-I{cu_path.parent}",
        "-o", str(so_path),
        str(cu_path),
    ]
    cmd.extend(extra_flags or [])
    cmd.extend(_auto_link_flags(cu_path))
    result = subprocess.run(
        cmd,
        capture_output=True,
        text=True,
        errors="replace",
        timeout=timeout,
        cwd=str(cu_path.parent),
    )
    if result.returncode != 0:
        raise RuntimeError(f"compile failed: {result.stderr[:4000]}")
    return so_path


def load_cuda_kernel(cu_file: str | Path, *, func_name: str, arch: str,
                     so_path: str | Path | None = None,
                     extra_flags: list[str] | None = None,
                     timeout: int = 120):
    shared = compile_cuda_shared(cu_file, arch=arch, output=so_path,
                                 extra_flags=extra_flags, timeout=timeout)
    lib = ctypes.CDLL(str(shared))
    try:
        return getattr(lib, func_name), shared
    except AttributeError as exc:
        raise RuntimeError(f"function '{func_name}' not found in {shared}") from exc


def run_cuda_correctness(func, *, shape_kwargs: dict[str, Any], dtype: torch.dtype,
                         device: str, epsilon: float, atol: float, rtol: float) -> dict[str, Any]:
    inputs = _generate_cuda_inputs(shape_kwargs, dtype=dtype, device=device, epsilon=epsilon)
    ref_out = reference_impl(inputs)
    launch_args, cuda_out = get_kernel_launch_args(inputs)
    func(*launch_args)
    torch.cuda.synchronize()
    result = validate_correctness(cuda_out, ref_out, atol=atol, rtol=rtol)
    result.update({
        "op": OPERATOR_NAME,
        "correct": result["passed"],
        "compile_ok": True,
        "max_abs_err": result["max_abs_error"],
        "max_rel_err": result["max_rel_error"],
        "message": "ok" if result["passed"] else "mismatch",
    })
    return result


def benchmark_cuda_kernel(func, *, n_warmup: int = 10, n_measure: int = 100,
                          shape_kwargs: dict[str, Any] | None = None,
                          dtype: torch.dtype = DEFAULT_DTYPE,
                          device: str = DEVICE,
                          epsilon: float = 1e-5) -> dict[str, Any]:
    inputs = _generate_cuda_inputs(shape_kwargs or {}, dtype=dtype, device=device, epsilon=epsilon)
    launch_args, _ = get_kernel_launch_args(inputs)
    for _ in range(n_warmup):
        func(*launch_args)
    torch.cuda.synchronize()
    starts = [torch.cuda.Event(enable_timing=True) for _ in range(n_measure)]
    ends = [torch.cuda.Event(enable_timing=True) for _ in range(n_measure)]
    for i in range(n_measure):
        starts[i].record()
        func(*launch_args)
        ends[i].record()
    torch.cuda.synchronize()
    times = sorted(float(s.elapsed_time(e)) for s, e in zip(starts, ends))
    p95_idx = min(len(times) - 1, int(len(times) * 0.95))
    avg = sum(times) / len(times)
    return {
        "op": OPERATOR_NAME,
        "latencies_ms": times,
        "latency_ms_median": times[len(times) // 2],
        "latency_ms_p95": times[p95_idx],
        "avg_ms": avg,
        "total_ms": sum(times),
        "warmup": n_warmup,
        "rounds": n_measure,
        "compile_ok": True,
    }


def benchmark_torch_reference(n_warmup: int = 10, n_measure: int = 100, **kw) -> dict[str, float]:
    inputs = generate_inputs(**kw)
    for _ in range(n_warmup):
        _ = reference_impl(inputs)
    torch.cuda.synchronize()
    starts = [torch.cuda.Event(enable_timing=True) for _ in range(n_measure)]
    ends = [torch.cuda.Event(enable_timing=True) for _ in range(n_measure)]
    for i in range(n_measure):
        starts[i].record(); _ = reference_impl(inputs); ends[i].record()
    torch.cuda.synchronize()
    times = sorted(s.elapsed_time(e) for s, e in zip(starts, ends))
    trim = max(1, len(times) // 10)
    t = times[trim:-trim] if len(times) > 2 * trim else times
    return {"mean_ms": sum(t)/len(t), "median_ms": t[len(t)//2],
            "min_ms": min(times), "max_ms": max(times)}


